#Деление отрицательных чисел, сложение и умножение строк, функции str и int

a = 11
b = 5
print('a = ', a,  'b = ', b)
print(a, '+', b, '=',  a + b)
print(a, '-', b, '=',  a - b)
print(a, '*', b, '=',  a * b)
print(a, '/', b, '=',  a / b)
print(a, '//', b, '=',  a // b)
print(a, '%', b, '=',  a % b)
print(a, '**', b, '=',  a ** b)